import gql from "graphql-tag";

export const SEARCH_PRODUCTS_QUERY = gql`
  query SEARCH_PRODUCTS($searchTerm: String!) {
    allProducts(
      where: {
        OR: [
          { name_contains_i: $searchTerm }
          { description_contains_i: $searchTerm }
        ]
      }
    ) {
      id
      name
      photo {
        image {
          publicUrlTransformed
        }
      }
    }
  }
`;
